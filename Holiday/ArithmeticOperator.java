package Holiday;

public class ArithmeticOperator {
    public static void main(String[] args) {
        int a = 10, b=5;
        
        int sum = a + b;
        int difference = a - b;
        int multiplay = a * b;
        int div = a / b;
        int mod = a % b;

        System.out.println("ผลบวก  : " + sum);
        System.out.println("ผลลบ  : " + difference);
        System.out.println("ผลคูณ : " + multiplay);
        System.out.println("ผลหาร : " + div);
        System.out.println("เศษที่เหลือ : " + mod);
        
    }
}
